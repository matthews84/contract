package com.whxisoft.pojo.proc;

public class CustomerForm {
    private String customerBasic;//基本
    private String customerContact;//联系
    private String customerCredit;//信用
    private String customerOther;//其他
    private String customerEnclosure;//附件
    private String customerPhoto;//照片

    public String getCustomerBasic() {
        return customerBasic;
    }

    public void setCustomerBasic(String customerBasic) {
        this.customerBasic = customerBasic;
    }

    public String getCustomerContact() {
        return customerContact;
    }

    public void setCustomerContact(String customerContact) {
        this.customerContact = customerContact;
    }

    public String getCustomerCredit() {
        return customerCredit;
    }

    public void setCustomerCredit(String customerCredit) {
        this.customerCredit = customerCredit;
    }

    public String getCustomerOther() {
        return customerOther;
    }

    public void setCustomerOther(String customerOther) {
        this.customerOther = customerOther;
    }

    public String getCustomerEnclosure() {
        return customerEnclosure;
    }

    public void setCustomerEnclosure(String customerEnclosure) {
        this.customerEnclosure = customerEnclosure;
    }

    public String getCustomerPhoto() {
        return customerPhoto;
    }

    public void setCustomerPhoto(String customerPhoto) {
        this.customerPhoto = customerPhoto;
    }
}
