package com.whxisoft.pojo.proc;

public class VendorForm {
    private String vendorBasic;//基本
    private String vendorTouch;//联系
    private String vendorCredit;//信用
    private String vendorOther;//其他
    private String vendorEnclosure;//附件

    public String getVendorBasic() {
        return vendorBasic;
    }
    public void setVendorBasic(String vendorBasic) {
        this.vendorBasic = vendorBasic;
    }
    public String getVendorTouch() {
        return vendorTouch;
    }
    public void setVendorTouch(String vendorTouch) {
        this.vendorTouch = vendorTouch;
    }
    public String getVendorCredit() {
        return vendorCredit;
    }

    public void setVendorCredit(String vendorCredit) {
        this.vendorCredit = vendorCredit;
    }

    public String getVendorOther() {
        return vendorOther;
    }

    public void setVendorOther(String vendorOther) {
        this.vendorOther = vendorOther;
    }

    public String getVendorEnclosure() {
        return vendorEnclosure;
    }

    public void setVendorEnclosure(String vendorEnclosure) {
        this.vendorEnclosure = vendorEnclosure;
    }
}
