package com.whxisoft.pojo.proc;

public class ContractOperForm {
    private String contractInfo; //合同概要
    private String contractItem; //合同标的
    private String contractPay; //收付计划
    private String contractCaluse; //合同条款
    private String contractMemo; //大事记
    private String contractExt; //附件

    public String getContractInfo() {
        return contractInfo;
    }

    public void setContractInfo(String contractInfo) {
        this.contractInfo = contractInfo;
    }

    public String getContractItem() {
        return contractItem;
    }

    public void setContractItem(String contractItem) {
        this.contractItem = contractItem;
    }

    public String getContractPay() {
        return contractPay;
    }

    public void setContractPay(String contractPay) {
        this.contractPay = contractPay;
    }

    public String getContractCaluse() {
        return contractCaluse;
    }

    public void setContractCaluse(String contractCaluse) {
        this.contractCaluse = contractCaluse;
    }

    public String getContractMemo() {
        return contractMemo;
    }

    public void setContractMemo(String contractMemo) {
        this.contractMemo = contractMemo;
    }

    public String getContractExt() {
        return contractExt;
    }

    public void setContractExt(String contractExt) {
        this.contractExt = contractExt;
    }
}
